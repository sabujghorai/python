roll = {"hello","world","sabuj"}
roll.add(1) # we can add one element by one parentheses
roll.add((2,3)) # we can add multiple elements by double parentheses
print(roll)

# remove method
roll.remove("hello") # removes the element 34
print(roll)
# pop method

print(roll.pop())
print(roll.pop())

# clear method
roll.clear()
print(roll)
